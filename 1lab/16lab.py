age = int(input("Введите возраст: "))

print(f"\nРезультаты проверки для возраста {age}:")

if age >= 18:
    print("Совершеннолетний (≥18 лет)")
else:
    print("Несовершеннолетний (<18 лет)")

if age >= 65:
    print("Пенсионный возраст (≥65 лет)")
else:
    print("Не пенсионный возраст (<65 лет)")

if 7 <= age < 18:
    print("Подходит для школы (7-17 лет)")
else:
    print("Не подходит для школы")

if age % 10 == 0 and age > 0:
    print("Возраст - круглый десяток")
else:
    print("Возраст не круглый десяток")